ORG 0
OUT Timer
LOADI 0
STORE POINTS

NEWSAMPLE:
IN Timer
STORE CUR_TIME
LOAD TIME_LIMIT
SUB CUR_TIME
OUT Hex0
JNEG DONE

LOAD POINTS
OUT Hex1

LOADI &H0022
STORE CONFIG
IN Switches
AND ONE
JZERO SKIP_1
LOADI &H0026
STORE CONFIG
SKIP_1:
LOAD NEXT_TARGET_LOCATION
SHIFT 3
STORE NEXT_TARGET_LOCATION
SUB LEFT
JNEG SKIP
LOADI 1
STORE NEXT_TARGET_LOCATION

SKIP:
LOAD CONFIG;
;; OUT Hex1
OUT ADC; 

SAMPLE:
IN ADC
SUB BITMASK
JZERO SAMPLE ; keep sampling if sentinel value is detected

IN ADC
AND MASK
CALL Light_LEDS
JUMP NEWSAMPLE


Light_LEDS:
STORE VAL
LOADI &H200
STORE LED_POS
LOADI &H019A
STORE THRESHOLD
CHECK:
LOAD VAL
SUB THRESHOLD
JNEG LIGHT
LOAD THRESHOLD
ADD STEP
STORE THRESHOLD
LOAD LED_POS
SHIFT -1
STORE LED_POS
JUMP CHECK
LIGHT:
LOAD LED_POS
OR TARGET_LOCATION
OUT LEDs
SUB TARGET_LOCATION
JNZ SKIP1
LOAD NEXT_TARGET_LOCATION
STORE TARGET_LOCATION
LOAD POINTS
ADDI 1
STORE POINTS
SKIP1:
RETURN


DONE:
JUMP DONE

ORG &H80
BITMASK: DW &H8800
CHANNEL: DW 0
MASK: DW &H0FFF
ONE: DW 1
CONFIG: DW 0

NUM_LEDS: DW &H000A
THRESHOLD: DW &H019A
STEP: DW &H019A
LED_POS: DW &H0001
VAL: DW 0
CUR_TIME: DW 0
TIME_LIMIT: DW 100

NEXT_TARGET_LOCATION: DW 1
TARGET_LOCATION: DW 1
LEFT: DW &H400

POINTS: DW 0

Switches:  EQU 000
LEDs:      EQU 001
Timer:     EQU 002
Hex0:      EQU 004
Hex1:      EQU 005
ADC: 		EQU &HC0
